    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        try
        {
            if(txtnombre.getText().isEmpty())
            {
                 throw new Exception("Ingrese el nombre del ser vivo");
            }
            if(txtdecripcion.getText().isEmpty())
            {
                 throw new Exception("Ingrese una descripcion");
            }
            SeresVivos obje = new SeresVivos();
            SeresVivos objeEqui  = (SeresVivos)this.cmbreferencia.getSelectedItem();
            obje.setDesSerVivo(this.txtdecripcion.getText());
            obje.setNombreSerVivo(this.txtnombre.getText());
            obje.setRefSerVivo(objeEqui.getIdSerVivo());
            if(cmbreferencia.getSelectedIndex() == 0)
            {
                if(new SeresVivosCtrl().guarR(obje))
                {
                    JOptionPane.showMessageDialog(this, "Datos guardados");
                    txtnombre.setText("");
                    txtdecripcion.setText("");
                    txtnombre.requestFocus();
                    cmbreferencia.setSelectedIndex(0);
                    cargarDatos();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Oops! algo malo pas贸");
                }
            }
            else
            {
                if(new SeresVivosCtrl().guar(obje))
                {
                    JOptionPane.showMessageDialog(this, "Datos guardados");

                    cargarDatos();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Oops! algo malo pas贸");
                }
            }
            
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int validar = JOptionPane.showConfirmDialog(this, "Decea eliminar este registro");
        if(validar == 1 || validar == 2 || validar == -1)
        {
            
        }
        else
        {
            try
            {
                if(txtid.getText().isEmpty())
                {
                    throw new Exception("Seleccione un registro a eliminar");
                }
                SeresVivos obje = new SeresVivos();
                obje.setIdSerVivo(Integer.parseInt(this.txtid.getText()));
                if(new SeresVivosCtrl().elim(obje))
                {
                    JOptionPane.showMessageDialog(this, "Datos Eliminados");
                    txtid.setText("");
                    txtnombre.setText("");
                    txtdecripcion.setText("");
                    txtnombre.requestFocus();
                    cmbreferencia.setSelectedIndex(0);
                    btnEliminar.setEnabled(false);
                    btnModificar.setEnabled(false);
                    btnNuevo.setEnabled(false);
                    btnguardar.setEnabled(true);
                    cargarDatos();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Oops! algo malo pas贸");
                }
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblSeresVivosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSeresVivosMouseClicked
        btnguardar.setEnabled(false);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnNuevo.setEnabled(true);
        int fila = this.tblSeresVivos.getSelectedRow();
        if(fila >= 0)
        {
            cmbreferencia.setEditable(true);
            cmbreferencia.setSelectedItem(this.tblSeresVivos.getValueAt(fila, 2));
            cmbreferencia.setEditable(false);
            SeresVivos obje = (SeresVivos)this.tblSeresVivos.getValueAt(fila, 0);
            this.txtid.setText(String.valueOf(obje.getIdSerVivo()));
            this.txtnombre.setText(obje.getNombreSerVivo());
            this.txtdecripcion.setText(String.valueOf(obje.getDesSerVivo()));
        }
    }//GEN-LAST:event_tblSeresVivosMouseClicked

