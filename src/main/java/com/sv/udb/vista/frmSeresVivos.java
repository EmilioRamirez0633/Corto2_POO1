    private void cargarDatos()
    {
         try
        {
            DefaultTableModel model = (DefaultTableModel)this.tblSeresVivos.getModel();
            while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo
            
            for(SeresVivos temp : new SeresVivosCtrl().consTodo())
            {
                if(temp.getIdSerVivo() !=0)
                {
                    model.addRow(new Object[]{temp,temp.getDesSerVivo(), temp.getRefNomSerVivo()});
                }
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    private void llenarCombo()
    {
         try
        {
            DefaultComboBoxModel<SeresVivos> model = new DefaultComboBoxModel<>();//            
            for(SeresVivos temp : new SeresVivosCtrl().consTodo())
            {
                model.addElement(temp);
            }
            this.cmbreferencia.setModel((DefaultComboBoxModel)model);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
